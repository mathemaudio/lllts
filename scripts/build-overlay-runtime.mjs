import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { build, context } from 'esbuild';

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDirectory, '..');
const watchMode = process.argv.includes('--watch');

const buildOptions = {
	entryPoints: [path.join(projectRoot, 'src', 'server', 'overlay-runtime', 'OverlayRuntimeBootstrap.lll.ts')],
	outfile: path.join(projectRoot, 'src', 'server', 'cdn', 'js', 'script.js'),
	bundle: true,
	platform: 'browser',
	format: 'iife',
	target: ['es2020'],
	legalComments: 'none',
	logLevel: 'info',
	tsconfig: path.join(projectRoot, 'tsconfig.overlay-runtime.json'),
	banner: {
		js: '// Generated by `pnpm -C lllts run build:overlay-runtime`. Do not edit this file directly.'
	}
};

if (watchMode) {
	const buildContext = await context(buildOptions);
	await buildContext.watch();
	console.log('[overlay-runtime] watching');
	const stopWatching = async () => {
		await buildContext.dispose();
		process.exit(0);
	};
	process.on('SIGINT', () => void stopWatching());
	process.on('SIGTERM', () => void stopWatching());
	await new Promise(() => {});
} else {
	await build(buildOptions);
}
